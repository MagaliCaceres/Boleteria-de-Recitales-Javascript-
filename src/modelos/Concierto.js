
// Clase que mediante su constructor creo los conciertos en el sitio
class Concierto {

    constructor(nombre, fecha, horario, precio, ubicacion, entradas){

        this.nombre = nombre;
        this. fecha = fecha;
        this.horario = horario;
        this.precio = precio;
        this.ubicacion = ubicacion;
        this.entradas = entradas;
    }
}

